    case SQUARE(side: Double)
    case RECTANGLE(side: Double, side2: Double)
    case TRIANGLE(side: Double, side2: Double)
    func getArea() -> Double{
        switch(self){
            case .SQUARE(let side):
                return side*side
            case .RECTANGLE(let side, let side2):
                return side*side2
            case .TRIANGLE(let side, let side2):
                return (side*side2) / 2
    }

}}  


