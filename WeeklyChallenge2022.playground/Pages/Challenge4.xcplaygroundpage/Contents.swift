protocol Polygon {
    func area() -> Double
}

struct Triangle: Polygon {
    var width: Double
    var height: Double
    
    func area() -> Double {
        (height * width) / 2
    }
}

struct Square: Polygon {
    var sideLength: Double
    
    func area() -> Double { pow(sideLength, 2) }
}

struct Rectangle: Polygon {
    var width: Double
    var height: Double
    
    func area() -> Double { width * height }
}

